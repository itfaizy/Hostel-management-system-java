    public void saveAuditLog(User user) {
        LocalDateTime now = LocalDateTime.now();
        String date = (now.format(DateTimeFormatter.ofPattern("MMM dd yyyy HH:mm:ss")));

        String content = String.format("%s;%s\n", user.getName(), date);
        File_Function.ModifyFile(content, ConfigControl.AUDITLOG_FILENAME, true);
    }

