    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        file = new File(ConfigControl.USER_FILENAME);
        usernameInput = usernameText.getText();
        passwordInput = passwordText.getText();

        boolean isSuccess = false;

        try {
            Scanner in = new Scanner(file);
            while (in.hasNextLine()) {
                String s = in.nextLine();
                String[] sArray = s.split(";");
                User user = User.getUser(sArray[0], 
                                        sArray[1], 
                                        sArray[2], 
                                        sArray[3], 
                                        sArray[4], 
                                        sArray[5],
                                        sArray[6],
                                        sArray[7]);
//                System.out.println(user);
                //polymorphism
//                System.out.println(user.getName());
//                System.out.println(usernameInput);
                if (user != null && usernameInput.equals(user.getUser()) && passwordInput.equals(user.getPassword())) {
                    JOptionPane.showMessageDialog(null,
                            "Login Successful", "Success",
                            JOptionPane.INFORMATION_MESSAGE);
                    isSuccess = true;

                    if (user.getRole() == (Roles.ADMIN)) {
                        dispose();
                        ConfigControl.CurrentUser = (Admin)user;
                        Admin_Main am = new Admin_Main();
                        am.setVisible(true);
                    } else if (user.getRole() == (Roles.STUDENT)) {
                        dispose();
                        ConfigControl.CurrentUser = (Student)user;
                        Student_Main sm = new Student_Main();
                        sm.setVisible(true);
                    }
                    if ((!user.getRole().equals(Roles.ADMIN)) && (!user.getRole().equals(Roles.STUDENT))){
                        saveAuditLog(user);
                    }
                    break;
                }
            }
            in.close();
            if (!isSuccess) {
                JOptionPane.showMessageDialog(null,
                        "Invalid Username / Password", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,
                    "User Not Found", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

